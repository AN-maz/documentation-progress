// AUGMENTED ASSIGNMENT

let x = 10;

x += 5; // x = x + 5
document.writeln(x); // 15
document.writeln("<br>");

x -= 3; // x = x - 3
document.writeln(x); // 12
document.writeln("<br>");

x *= 2; // x = x * 2
document.writeln(x); // 24
document.writeln("<br>");

x /= 4; // x = x / 4
document.writeln(x); // 6
document.writeln("<br>");

x %= 5; // x = x % 5
document.writeln(x); // 1

document.writeln("<hr>");
