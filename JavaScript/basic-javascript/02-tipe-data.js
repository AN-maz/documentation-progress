// TIPE DATA

// Number (angka)
document.writeln(100);
document.writeln("<br>");
document.writeln(100.101);

// Number Notation
document.writeln("<br>");

// Biner (basis 2)
document.writeln(0b1111010); // 122

// Hexadecimal (basis 16)
document.writeln("<br>");
document.writeln(0x7A); // 122

// Octal (basis 8)
document.writeln("<br>");
document.writeln(0o172); // 122

document.writeln("<hr>");

// STRING 

document.writeln("Hello");
document.writeln("<br>");
document.writeln('Belajar JavaScript');
document.writeln("<br>");
document.writeln('Saya: ' + 'Seorang ' + ' Programmer');


// Escape Character
document.writeln("<br>");
document.writeln("Nama: \"ANmaz\"");
document.writeln("<br>");
document.writeln('Ini kutip satu: \'JS\'');
document.writeln("<hr>");

// BOOLEAN
document.writeln(true);
document.writeln("<br>");
document.writeln(false);

document.writeln("<hr>");
