    public void selesaiSesiOperator(String operator, int skorSesi) {
        operatorSelesai.add(operator);
        totalSkorUjian += skorSesi;

        // 1. Hapus operator yang sudah beres dari ComboBox agar tidak bisa dipilih lagi
        cb_operator.removeItem(operator);

        int sisa = 4 - operatorSelesai.size();

        if (operatorSelesai.size() == 4) {
            lbl_status_ujian.setText("Status: Semua ujian selesai!");
            javax.swing.JOptionPane.showMessageDialog(this, "Selamat! Kamu telah menyelesaikan semua 4 tipe soal.\nTotal Skor Akhir: " + totalSkorUjian);

            // Simpan skor ke database
            pengerjaanDAO.simpanHasil(namaUserAktif, totalSkorUjian);
            loadDataTabel();

            // RESET SISTEM (Agar user bisa ikut ujian gelombang 2 jika mau)
            operatorSelesai.clear();
            totalSkorUjian = 0;
            lbl_status_ujian.setText("Status: 4 tipe soal belum dikerjakan");

            // Kembalikan isi ComboBox seperti semula
            cb_operator.removeAllItems();
            cb_operator.addItem("+");
            cb_operator.addItem("-");
            cb_operator.addItem("*");
            cb_operator.addItem("/");

            this.setVisible(true);
        } else {
            // 2. Update teks pemberitahuan sisa soal di Dashboard
            lbl_status_ujian.setText("Status: Sisa " + sisa + " tipe soal");
            javax.swing.JOptionPane.showMessageDialog(this, "Soal " + operator + " selesai!\nSisa tipe soal yang belum dikerjakan: " + sisa);
            this.setVisible(true);
        }
    }
