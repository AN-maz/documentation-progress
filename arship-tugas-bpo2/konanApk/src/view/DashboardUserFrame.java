    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String operatorPilihan = cb_operator.getSelectedItem().toString();

        // 1. Cek apakah operator ini sudah dikerjakan sebelumnya
        if (operatorSelesai.contains(operatorPilihan)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Kamu sudah menyelesaikan soal " + operatorPilihan + "! Silakan pilih operator lain.");
            return;
        }

        // 2. Coba kunci operatornya
        if (pengerjaanDAO.kunciOperator(operatorPilihan, namaUserAktif)) {

            // HIDE dashboard (jangan dispose), lalu kirim 'this' (diri sendiri) ke QuizFrame
            QuizFrame kuis = new QuizFrame(namaUserAktif, operatorPilihan, this);
            kuis.setLocationRelativeTo(null);
            kuis.setVisible(true);
            this.setVisible(false);

        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Maaf, soal " + operatorPilihan + " sedang dikerjakan oleh orang lain. Tunggu sebentar atau pilih soal lain!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

