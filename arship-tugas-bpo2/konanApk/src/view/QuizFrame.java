    private void jalankanTimer() {
        timerWaktu = new Timer(1000, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                detikTersisa--;
                int menit = detikTersisa / 60;
                int detik = detikTersisa % 60;

                // Update JLabel waktu di UI (Pastikan sudah buat lbl_waktu di NetBeans)
                lbl_waktu.setText(String.format("Waktu: %02d:%02d", menit, detik));

                if (detikTersisa <= 0) {
                    timerWaktu.stop();
                    JOptionPane.showMessageDialog(null, "Waktu Habis!");
                    selesaiKuis(); // Paksa selesai jika waktu habis
                }
            }
        });
        timerWaktu.start();
    }

    public void generateSoal() {
        tf_jawab.setText("");
        tf_jawab.requestFocus();

        Random rand = new Random();
        int angka1 = rand.nextInt(100) + 1;
        int angka2 = rand.nextInt(100) + 1;

        // KALKULASI KINI HANYA MENGGUNAKAN OPERATOR YANG DIPILIH
        switch (operatorKuis) {
            case "+":
                jawabanBenar = angka1 + angka2;
                break;
            case "-":
                jawabanBenar = angka1 - angka2;
                break;
            case "*":
                jawabanBenar = angka1 * angka2;
                break;
            case "/":
                jawabanBenar = (double) angka1 / angka2;
                break;
        }

        txtSoal.setText("Soal " + nomorSoal + "/10 : " + angka1 + " " + operatorKuis + " " + angka2 + " = ?");
    }

    private void selesaiKuis() {
        if (timerWaktu != null) {
            PengerjaanDAO dao = new PengerjaanDAO();
            dao.bukaKunciOperator(operatorKuis);

            // 2. Lempar skor kembali ke Dashboard (TIDAK ADA simpanHasil di sini)
            dashboardAsal.selesaiSesiOperator(operatorKuis, skor);

            // 3. Hancurkan frame kuis saja
            this.dispose();
        }
    }

