    private void btn_jawabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_jawabActionPerformed
        // TODO add your handling code here:
        String input = tf_jawab.getText().trim();

        if (input.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Isi jawabanmu dulu!");
            return;
        }

        try {
            double jawabanUser = Double.parseDouble(input);

            // Cek kebenaran (toleransi desimal untuk pembagian)
            if (Math.abs(jawabanUser - jawabanBenar) < 0.01) {
                skor += 10;
            }

            // Cek apakah kuis sudah mencapai 10 soal
            if (nomorSoal == 10) {
                selesaiKuis();
            } else {
                nomorSoal++; // Lanjut ke soal berikutnya
                generateSoal();
            }

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Masukkan format angka yang valid!");
        }
    }//GEN-LAST:event_btn_jawabActionPerformed

