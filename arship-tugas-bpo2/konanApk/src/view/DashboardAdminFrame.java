    public void loadDataTabel() {
        DefaultTableModel model = (DefaultTableModel) tabelHasil.getModel();
        model.setRowCount(0);

        // Ambil data yang HANYA dimiliki oleh user yang sedang login
        List<Pengerjaan> daftarPengerjaan = pengerjaanDAO.ambilSemuaHasil();

        for (Pengerjaan p : daftarPengerjaan) {
            Object[] row = {
                p.getIdPengerjaan(),
                p.getPengguna(),
                p.getSkor(),
                p.getTanggalPengerjaan(),
                p.getJamPengerjaan()
            };
            model.addRow(row);
        }
    }

