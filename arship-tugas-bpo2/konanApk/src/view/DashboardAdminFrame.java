    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        int barisTerpilih = tabelHasil.getSelectedRow();
        if (barisTerpilih == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data yang ingin dihapus terlebih dahulu di tabel!");
            return;
        }

        // 2. Ambil ID Pengerjaan dari kolom pertama (index 0) pada baris yang dipilih
        int idPengerjaan = (int) tabelHasil.getValueAt(barisTerpilih, 0);

        // 3. Tampilkan dialog konfirmasi
        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus data ini?", "Konfirmasi Hapus", javax.swing.JOptionPane.YES_NO_OPTION);

        // 4. Jika user klik "Yes", eksekusi fungsi DAO
        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            if (pengerjaanDAO.hapusPengerjaan(idPengerjaan)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
                loadDataTabel(); // Refresh tabel agar data yang terhapus hilang dari layar
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "Gagal menghapus data!");
            }
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        int barisTerpilih = tabelHasil.getSelectedRow();
        if (barisTerpilih == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data yang ingin diubah terlebih dahulu di tabel!");
            return;
        }

        // 2. Ambil ID Pengerjaan dan Skor Lama dari tabel
        int idPengerjaan = (int) tabelHasil.getValueAt(barisTerpilih, 0);
        int skorLama = (int) tabelHasil.getValueAt(barisTerpilih, 2); // Skor ada di index kolom 2

        // 3. Tampilkan input dialog untuk memasukkan skor baru
        String inputSkorBaru = javax.swing.JOptionPane.showInputDialog(this, "Ubah Skor:", skorLama);

        // 4. Validasi dan eksekusi
        if (inputSkorBaru != null && !inputSkorBaru.trim().isEmpty()) {
            try {
                int skorBaru = Integer.parseInt(inputSkorBaru); // Pastikan inputan adalah angka
                if (pengerjaanDAO.ubahSkor(idPengerjaan, skorBaru)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Skor berhasil diperbarui!");
                    loadDataTabel(); // Refresh tabel
                } else {
                    javax.swing.JOptionPane.showMessageDialog(this, "Gagal mengubah skor!");
                }
            } catch (NumberFormatException e) {
                javax.swing.JOptionPane.showMessageDialog(this, "Error: Skor harus berupa angka bulat!");
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

